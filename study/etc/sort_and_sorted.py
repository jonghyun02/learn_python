# sort()
# 기능: 리스트 자체를 정렬합니다.
# 동작 방식: 리스트 객체에서 직접 호출되는 메소드로, 리스트의 순서를 변경합니다.
# 반환 값: None. 원본 리스트를 직접 변경합니다.
numbers = [3, 1, 4, 1, 5, 9]
numbers.sort()
print(numbers)  # [1, 1, 3, 4, 5, 9]


# sorted()
# 기능: 리스트를 정렬한 새로운 리스트를 반환합니다.
# 동작 방식: 리스트의 원본을 변경하지 않고 정렬된 새 리스트를 반환합니다.
# 반환 값: 정렬된 새로운 리스트.
numbers = [3, 1, 4, 1, 5, 9]
sorted_numbers = sorted(numbers)
print(sorted_numbers)  # [1, 1, 3, 4, 5, 9]
print(numbers)         # [3, 1, 4, 1, 5, 9] (원본 리스트는 변경되지 않음)